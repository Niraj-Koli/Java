package Switch;

public class Switch {
    public static void main(String[] args) {
        String x = "Nero";

        switch (x) {
            case "Kaizer":
                System.out.println("Kaizer");
                break;
            case "Nero":
                System.out.println("Nero");
                break;
            case "Dante":
                System.out.println("Dante");
                break;
            default:
                System.out.println("Noob");
                break;
        }
    }
}
